<?php

/**
 * A pair with two elements.
 */
class Pair
{
    /**
     * First value of the pair.
     *
     * @var mixed $first
     */
    public $first;

    /**
     * Second value of the pair.
     *
     * @var mixed $second
     */
    public $second;

    /**
     * Constructor.
     *
     * @param mixed $first
     * @param mixed $second
     */
    function __construct($first, $second) {}
}